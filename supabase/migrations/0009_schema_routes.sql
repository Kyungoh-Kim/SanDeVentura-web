-- ── 7. hiking_sessions: optional route association ────────────────────────────

alter table public.hiking_sessions
  add column route_id text references public.routes(id);

-- ── 8. RLS ────────────────────────────────────────────────────────────────────

alter table public.routes enable row level security;

create policy "Users can read route catalog"
  on public.routes
  for select
  using (true);
